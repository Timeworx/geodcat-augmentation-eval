from . import vocab
from .types import SegmentationType


def build_legacy_skeleton(
    *,
    job_id: str,
    segmentation_type: SegmentationType,
    raw_uri: str,
    target_class: str,
    coco_access_url: str,
) -> dict:
    """Build a compliant GeoDCAT-AP skeleton for the Legacy path.

    Differences from the AI-Ready augmented graph (per the brief §8.2):
    - No dct:spatial / dct:temporal.
    - dcat:keyword is literal strings only (no skos:Concept mapping).
    - prov:wasDerivedFrom points at the raw URI itself with a short
      description, not a structured @id node.
    """
    dataset_iri = f"{vocab.TIMEWORX_DATASET_NS}/{job_id}"
    seg_pretty = segmentation_type.pretty
    return {
        "@context": {
            "dcat": "http://www.w3.org/ns/dcat#",
            "dct": "http://purl.org/dc/terms/",
            "foaf": "http://xmlns.com/foaf/0.1/",
            "prov": "http://www.w3.org/ns/prov#",
            "rdfs": "http://www.w3.org/2000/01/rdf-schema#",
        },
        "@graph": [
            {
                "@id": dataset_iri,
                "@type": "dcat:Dataset",
                "dct:title": f"Timeworx {seg_pretty} Segmentation: {target_class}",
                "dct:description": (
                    f"Automated {seg_pretty.lower()} segmentation masks generated by "
                    f"Timeworx.io from client-provided raw imagery. Target class: {target_class}."
                ),
                "dct:identifier": job_id,
                "dct:publisher": vocab.timeworx_publisher(),
                "dcat:keyword": [
                    {"@language": "en", "@value": f"{seg_pretty} Segmentation"},
                    {"@language": "en", "@value": target_class},
                    {"@language": "en", "@value": "Computer Vision"},
                ],
                "prov:wasDerivedFrom": {
                    "@id": raw_uri,
                    "dct:description": "Client-provided raw imagery URI",
                },
                "dcat:distribution": [vocab.coco_distribution(dataset_iri, coco_access_url)],
            }
        ],
    }
